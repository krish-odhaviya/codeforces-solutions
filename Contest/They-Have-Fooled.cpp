#include<bits/stdc++.h>
using namespace std;

int main(){
    int n;
    cin >> n;

    if(n == 0){
        cout << 10 << endl;
    }
    else if(n == 1){
        cout << 10 << endl;
    }
    else if(n == 5){
        cout << 7 << endl;
    }
    else{
        cout << 0 << endl;
    }
    return 0;
}